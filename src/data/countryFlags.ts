/**
 * Returns the circle-flag SVG URL for a known country crew name, or null.
 */
export function getCircleFlagUrl(crewName: string): string | null {
  const code = COUNTRY_CODE_MAP[crewName];
  if (!code) return null;
  return `https://hatscripts.github.io/circle-flags/flags/${code}.svg`;
}

/**
 * Returns true if the crew name matches a known country.
 */
export function isCountryName(crewName: string): boolean {
  return crewName in COUNTRY_CODE_MAP;
}
